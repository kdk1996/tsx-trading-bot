# --- 7. DEEP DIVE TREND ANALYSIS ---
st.divider()
st.subheader("📊 High-Precision Trend Charting")

AV_API_KEY = "UG99FWTXZTMOSVFD" 

@st.cache_data(ttl=3600)
def get_chart(ticker):
    try:
        av_ticker = ticker.replace(".TO", ".TRT")
        ts = TimeSeries(key=AV_API_KEY, output_format='pandas')
        chart_data, _ = ts.get_daily(symbol=av_ticker)
        chart_data.columns = ['Open', 'High', 'Low', 'Close', 'Volume']
        return chart_data.head(60)
    except:
        return None

selected = st.selectbox("Select a stock for detailed analysis:", ["SHOP.TO", "RY.TO", "ENB.TO", "VET.TO", "CSU.TO"])

if selected:
    with st.spinner('Syncing with market data...'):
        history = get_chart(selected)
    
    if history is not None and not history.empty:
        fig = go.Figure(data=[go.Candlestick(
            x=history.index,
            open=history['Open'],
            high=history['High'],
            low=history['Low'],
            close=history['Close'],
            increasing_line_color='#008148', 
            decreasing_line_color='#d90429'
        )])
        fig.update_layout(
            template="plotly_white",
            height=500,
            xaxis_rangeslider_visible=False,
            margin=dict(l=10, r=10, t=10, b=10)
        )
        st.plotly_chart(fig, use_container_width=True)
    else:
        st.error("Live charting limit reached. Please check back in a few minutes.")
