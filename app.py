# --- 6. LIVE P&L LEDGER (The Trainer) ---
if data is not None:
    st.subheader("🤖 Strategy Ledger & Profit/Loss Analysis")
    st.info("The AI uses these results to 'learn' which signals provide the highest returns.")
    
    def style_pnl(val):
        if isinstance(val, (int, float)):
            if val > 0: return 'color: #008148; font-weight: bold;' # Profit Green
            if val < 0: return 'color: #d90429; font-weight: bold;' # Loss Red
        return 'color: #0a192f;'

    # Clean up and display columns
    cols_to_show = ['Name', 'Ticker', 'Price', 'Signal', 'Bought_At', 'P&L_%']
    display_df = data[cols_to_show]
    
    try:
        styled_table = display_df.style.map(style_pnl, subset=['P&L_%'])
    except AttributeError:
        styled_table = display_df.style.applymap(style_pnl, subset=['P&L_%'])
        
    st.table(styled_table)
